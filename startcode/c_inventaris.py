class Auto:
    def __init__(self, merk, model, jaar):
        self.merk = merk
        self.model = model
        self.jaar = jaar

        def toon_informatie(self):
            print(f"Naam: {self.merk}")
            print(f"model:  {self.model}")
            print(f"jaar: {self.jaar}")




